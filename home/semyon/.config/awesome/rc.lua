	batwidget,
	vicious.widgets.bat,
	function (widget, args)
		local i = { '⮒', '⮏', '⮐', '⮑', '⮍', '⮎' }
        local is_discharging = args[1] == "-"
        local is_charging = args[1] == "+"
		local ico =
			is_discharging and i[ math.ceil(args[2]/25) + 1 ]
