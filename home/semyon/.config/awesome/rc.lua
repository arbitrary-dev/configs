function set_wallpaper(s)
    -- Wallpaper
    if beautiful.wallpaper then
        local wallpaper = beautiful.wallpaper
        -- If wallpaper is a function, call it with the screen
        if type(wallpaper) == "function" then
            wallpaper = wallpaper(s)
        end
        gears.wallpaper.maximized(wallpaper, s, true)
    elseif beautiful.wallpapers_dir then
        local dir = beautiful.wallpapers_dir
        local wps = {}

        local h_dir = io.popen("ls " .. dir)
        for line in h_dir:lines() do
            table.insert(wps, line)
        end
        h_dir:close()

        -- pick random from dir
        math.randomseed(os.time())
        local wp = dir .. '/' .. wps[math.random(1, #wps)]
        gears.wallpaper.maximized(wp, s, true)
    end
end

-- Re-set wallpaper when a screen's geometry changes (e.g. different resolution)
screen.connect_signal("property::geometry", set_wallpaper)

awful.screen.connect_for_each_screen(function(s)
    -- Wallpaper
    set_wallpaper(s)

    -- Each screen has its own tag table.
    awful.tag({ "misc", "work", "web", "im" }, s, awful.layout.layouts[1])

    -- Create a promptbox for each screen
    s.mypromptbox = awful.widget.prompt()

    local prev_sahe = s.mypromptbox.spawn_and_handle_error
    local m = wibox.container.margin(s.mypromptbox, beautiful.sep_pad, 0, 0, 0)
    local bg = wibox.container.background(m)

    s.mypromptbox.spawn_and_handle_error = function(...)
        prev_sahe(...)
        bg:set_bg(beautiful.bg_normal)
        m:set_right(0)
    end

    s.mypromptbox = bg

    -- Create an imagebox widget which will contain an icon indicating which layout we're using.
    -- We need one layoutbox per screen.
    s.mylayoutbox = awful.widget.layoutbox(s)
    s.mylayoutbox:buttons(gears.table.join(
                           awful.button({ }, 1, function () awful.layout.inc( 1) end),
                           awful.button({ }, 3, function () awful.layout.inc(-1) end),
                           awful.button({ }, 4, function () awful.layout.inc( 1) end),
                           awful.button({ }, 5, function () awful.layout.inc(-1) end)))
    -- Create a taglist widget
    s.mytaglist = awful.widget.taglist(s, awful.widget.taglist.filter.all, taglist_buttons)
    s.mytaglist = wibox.container.margin(s.mytaglist, 0, 2, 0, 0)

    -- Create a tasklist widget
    s.mytasklist = awful.widget.tasklist(
		s,
		awful.widget.tasklist.filter.minimizedcurrenttags,
		tasklist_buttons,
        nil,
        function(w, buttons, label, data, objects)
            common.list_update(w, buttons, label, data, objects)
            for i, o in ipairs(objects) do
                if i % 2 == 1 then
                    local bg = data[o].bgb
                    bg:set_bg(beautiful.shade)
                end
                local m = data[o].m
                m:set_left(beautiful.sep_pad)
                m:set_right(beautiful.sep_pad)
            end
        end,
        wibox.layout.fixed.horizontal())

    -- Create the wibox
    s.mywibox = awful.wibar({ position = "top", height = 13, screen = s })

    -- Add widgets to the wibox
    s.mywibox:setup {
        layout = wibox.layout.align.horizontal,
        { -- Left widgets
            layout = wibox.layout.fixed.horizontal,
            s.mytaglist,
            s.mypromptbox,
        },
        { -- Middle widget
            layout = wibox.layout.align.horizontal,
            expand = "outside",
            nil,
            s.mytasklist
        },
        { -- Right widgets
            layout = wibox.layout.fixed.horizontal,
            separator,
            memwidget,
            separator,
            batwidget,
            separator,
            wifiwidget, -- separator embedded
            volwidget,
            separator,
            mytextclock,
        },
    }
end)
